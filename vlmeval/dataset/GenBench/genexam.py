prompt_for_eval = """
You are an expert-level evaluator for multi-disciplinary text-to-image generation. You will be given an input prompt for text-to-image generation, an image generated by a model (the first image), a ground truth image for reference (the second image), and several scoring point questions and their corresponding scores. Your task is to evaluate the generated image by answering the scoring point questions and evaluating the image from some global perspectives.

## Instructions
1. Remember that the first image is the generated image from a model, and the second image is the ground truth image. You should **only evaluate the generated image**, while the ground truth image can be used for reference.
2. First, give a detailed description of all the components in the generated image.
3. For each scoring point, you should use the ground truth image as **reference information** to make more accurate judgments. You should generate a **detailed** reasoning **step by step**. It should first analyze the question, what subject knowledge it requires (some questions may not explicitly include all the knowledge-related details, e.g. the shape of musical signs, the structure of molecules, etc.) and what information you need to refer to from the ground truth image, then analyze whether the generated image satisfies the scoring point based on the scoring point, prompt and reference information. Finally, answer 1 if the generated image fully satisfies the scoring point, otherwise answer 0. For example, if a scoring point is "Does the cell membrane label correspond to the correct position in the cell?", you can refer to the ground truth image to check the correct position of the cell membrane label as an example, by **explicitly analyzing the ground truth image** in your reasoning.
4. The answer is only based on the information in the generated image. Do not implicate or reason about more information, e.g. If the question is "Is the force 10N?", and the generated image does not mark the magnitude of the force, the answer should be 0. For some information (e.g. angle, length, perpendicular, etc.), you should not only rely on explicit marks in the image (if any), but also analyze the image itself, e.g. if the question is "Is the angle 90°?", you should not only check the text label, but also analyze the degree of the angle based on the image.
5. **Do not focus too much or be too strict on the text labels.** If some information is not explicitly marked or labeled in the image, you should analyze the image itself to check the information, e.g. whether the value or relationships of angles, segments, etc.  For instance, do not judge "AB = AC" as incorrect because the length of AB and AC is not marked in the image.
6. When checking information quantitative relationships like angles, lengths, coordinates, etc. in the image, do not be too strict. Only a rough fulfillment is required, and minor drawing errors are allowed.
7. You should also evaluate the generated image from some global perspectives provided below. For each perspective, first provide a **detailed step-by-step** reasoning, e.g. recognize all the text labels for spelling, then give a score in the defined range. When checking these global perspectives, you should check the generated image only and **do not check its consistency with the input prompt**.


## Global Perspectives
1. Spelling (range: 0-2): The spelling of the text in the image, including the notations and equations. You should first recognize the text in the image in the reasoning, then check the spelling of the text. This should not be affected by the resolution of the image  (e.g. the image is blurry), unless key information is not identifiable. Specifically:
- 0: There are critical errors in spelling, notations or equations which significantly hinders the understanding of the image.
- 1: There are some errors in spelling, notations or equations that somehow hinder the understanding of key information in the image.
- 2: All or almost all the spelling, notations and equations are correct. Tiny errors like spaces, commas, capital letters, etc. are allowed.
2. Clarity and Readability (range: 0-2): The clarity and readability of the image. Each component in the image should be clearly readable and identifiable. All text labels and marks should in the right place and are not overlapped or occluded by other elements. If the image is geometry or diagram, there should not be unlabeled points or lines or duplicated labels. If the image is a plot or chart, the axis should be labeled and the ticks should be carefully checked. You should first identify the components, labels, and marks in the image in the reasoning, then check their clarity and readability. This should not be affected by the resolution of the image  (e.g. the image is blurry), unless key information is not identifiable. Specifically:
- 0: There are critical clarity and readability issues which significantly hinders the understanding of the image e.g. some components are impossible to distinguish, or some labels are fully overlapped or occluded by other elements, or many key information are not labeled.
- 1: There are some clarity and readability issues, e.g. some components are not clearly readable and identifiable, or some labels are overlapped or occluded, or some key information are not labeled.
- 2: The clarity and readability is perfect or almost perfect. Components are clearly readable and identifiable and necessary labels and marks are present. Tiny errors are allowed.
3. Logical Consistency (range: 0-2): The logical consistency of the image. Check the correctness of all the marks, text, musical notes, etc. If the image is geometry, check the correctness of each marked angles, lengths, coordinates, etc. If the image is a plot or chart, check the correctness of each data point, the axis, the ticks, the legend, etc. You should first identify the components, labels, and marks in the image in the reasoning, then check their logical consistency. This should not be affected by the resolution of the image (e.g. the image is blurry), unless key information is not identifiable. Specifically:
- 0: There are critical logical consistency issues which significantly hinders the understanding of the image, e.g. some key marks are not correct, some angles/lengths/data points are significantly inconsistent with the text label, etc.
- 1: There are some logical consistency issues that somehow hinder the understanding of key information in the image, e.g. some marks are not correct, some angles/lengths/data points are inconsistent with the text label, etc.
- 2: The logical consistency is perfect or almost perfect. Marks, text, musical notes, etc. are correct and consistent. Tiny errors are allowed.

## Output Format
Respond STRICTLY in the following format:
{{
    "description": "...",
    "answers": [
        {{
            "reasoning": "...",
            "answer": 1
        }},
        {{
            "reasoning": "...",
            "answer": 0
        }},
        ...
    ],
    "global_evaluation": {{
        "Spelling": {{
            "reasoning": "...",
            "score": 0,
        }},
        "Clarity and Readability": {{
            "reasoning": "...",
            "score": 0,
        }},
        "Logical Consistency": {{
            "reasoning": "...",
            "score": 0,
        }},
    }}
}}

Make sure your response follows the format strictly and can be parsed by Python json.loads. Do not include any other explanation or formatting. Do not including any special marks (", ', `, etc.) in the reasoning or description.

## Prompt
{prompt}

## Scoring Points
{scoring_points}

"""  # noqa: E501


def legal_response_struct(item):
    if not isinstance(item, dict):
        return False
    description_flag = 'description' in item and isinstance(item['description'], str)
    answers_flag = True
    if 'answers' in item and isinstance(item['answers'], list):
        for ans in item['answers']:
            answers_flag *= ('reasoning' in ans and isinstance(ans['reasoning'], str))
            answers_flag *= ('answer' in ans and isinstance(ans['answer'], int))
            if not answers_flag:
                break
    else:
        answers_flag = False
    global_evaluation_flag = True
    if 'global_evaluation' in item and isinstance(item['global_evaluation'], dict):
        for key in ['Spelling', 'Clarity and Readability', 'Logical Consistency']:
            global_evaluation_flag *= (key in item['global_evaluation'] and isinstance(item['global_evaluation'][key], dict))  # noqa: E501
            if not global_evaluation_flag:
                break
            global_evaluation_flag *= ('reasoning' in item['global_evaluation'][key] and isinstance(item['global_evaluation'][key]['reasoning'], str))  # noqa: E501
            global_evaluation_flag *= ('score' in item['global_evaluation'][key] and isinstance(item['global_evaluation'][key]['score'], int))  # noqa: E501
            if not global_evaluation_flag:
                break
    else:
        global_evaluation_flag = False
    return description_flag and answers_flag and global_evaluation_flag


class GenExam(GenBaseTextDataset):
    """GenExam dataset wrapper for image generation and scoring."""

    TYPE = 'T2I'
    NUM_GENERATIONS = 1
    DEFAULT_JUDGE = 'gpt-5'
    # DEFAULT_JUDGE = 'gpt-5-2025-08-07'

    DATASET_URL = {
        'GenExam': 'https://opencompass.openxlab.space/utils/GenEval/genexam.tsv'
    }

    DATASET_MD5 = {
        'GenExam': '34f7651d443e8a027b627fc9e29ba4f9',
    }

    def build_prompt(self, line):
        """Build a text-only prompt list for JanusGeneration.generate_inner."""
        if isinstance(line, int):
            line = self.data.iloc[line]
        prompt_text = line['prompt']
        messages = []
        messages.append(dict(type='text', value=prompt_text))
        return messages

    @classmethod
    def evaluate_sample(self, judge_model, sample):
        if isinstance(sample, pd.Series):
            sample = sample.to_dict()
        index = sample['index']

        img = self.extract_single_image_from_response(sample['prediction'])

        if img is None:
            print(f'Failed to generate image for index {index}, skip evaluation.')
            return dict(
                description='generation failed',
                global_evaluation='generation failed',
                answers='generation failed')

        t2i_prompt = sample['prompt']

        assert sample["scoring_points"].startswith('['), f"Invalid scoring_points: {sample['scoring_points']}"
        scoring_points = eval(sample["scoring_points"])
        scoring_points = [item["question"] for item in scoring_points]
        score_prompt = prompt_for_eval.format(prompt=t2i_prompt, scoring_points=scoring_points)

        score_messages = [
            {'type': 'text', 'value': score_prompt},
            {'type': 'image','value': img},
            {'type': 'image','value': f"data:image/jpeg;base64,{sample['image']}"},
        ]

        retry = 3
        resp = judge_model.generate(message=score_messages)
        score = parse_json(resp)

        while (not legal_response_struct(score)) and retry > 0:
            retry -= 1
            resp = judge_model.generate(message=score_messages)
            score = parse_json(resp)
        if not legal_response_struct(score):
            print(f'Failed to parse score for index {index}, skip evaluation.')
            return dict(
                description='parsing failed',
                global_evaluation='parsing failed',
                answers='parsing failed')
        return score

    def evaluate(self, eval_file, **judge_kwargs):
        """Score generated images with GPT-5 and aggregate GenExam metrics.

        eval_file: .pkl with columns at least ['index', 'prediction'] where
                   'prediction' is a PIL.Image.Image.
        """
        judge = judge_kwargs['model']
        nproc = judge_kwargs.pop('nproc', 16)
        _ = judge_kwargs.pop('verbose', None)
        _ = judge_kwargs.pop('retry', None)

        tmp_file = get_intermediate_file_path(eval_file, f'_{judge}_tmp', 'pkl')
        score_file = get_intermediate_file_path(eval_file, f'_{judge}', 'tsv')
        rating_file = get_intermediate_file_path(eval_file, f'_{judge}_rating', 'json')

        judge_kwargs['temperature'] = 1
        judge_kwargs['reasoning_effort'] = 'low'
        judge_kwargs['max_tokens'] = 16384
        judge_kwargs['timeout'] = 600
        model = build_judge(**judge_kwargs)  # model card

        eval_df = load(eval_file)
        eval_df['index'] = eval_df['index'].astype(str)

        if not osp.exists(score_file):
            # resume support: load tmp results and drop failures
            res = {} if not osp.exists(tmp_file) else load(tmp_file)
            res = {
                k: v for k, v in res.items()
                if legal_response_struct(v)
            }

            todo_mask = ~eval_df['index'].isin(res.keys())
            data_un = eval_df[todo_mask].reset_index(drop=True)

            lt = len(data_un)
            # Build plain string prompts to avoid BaseAPI 'value' key issues
            if lt > 0:
                samples = [data_un.iloc[i] for i in range(len(data_un))]
                indices = [x['index'] for x in samples]
                jobs = [dict(judge_model=model, sample=sample) for sample in samples]
                _ = track_progress_rich(
                    self.evaluate_sample,  # callable(judge_model: BaseAPI, sample: dict) -> dict
                    jobs,      # iterable of dicts: {'judge_model': BaseAPI, 'sample': dict}
                    keys=indices,        # map results by 'index'
                    save=tmp_file,    # resume file
                    nproc=nproc,
                )
                score_map = load(tmp_file) if osp.exists(tmp_file) else {}
                score_map.update(res)
            else:
                score_map = res

            for k in ['description','global_evaluation', 'answers']:
                eval_df[k] = [score_map[idx][k] for idx in eval_df['index']]
            dump(eval_df, score_file)

        if osp.exists(tmp_file):
            os.remove(tmp_file)

        results = load(score_file)
        rating = self.calculate_score(results)
        dump(rating, rating_file)
        return rating

    def cal_score_single(self, eval_result):
        assert "global_evaluation" in eval_result and "answers" in eval_result, f"Invalid eval result: {eval_result}"  # noqa: E501
        scoring_point_scores = 0
        assert eval_result["scoring_points"].startswith('['), f"Invalid scoring_points: {eval_result['scoring_points']}"  # noqa: E501
        scoring_points = eval(eval_result["scoring_points"])

        if eval_result['answers'] in ['generation failed', 'parsing failed']:
            return {
                "semantic_correctness": 0,
                "readability": 0,
                "logical_consistency": 0,
                "spelling": 0,
                "scoring_point_all_correct": False,
                "strict_score": 0,
                "relaxed_score": 0,
                "error": True,
            }
        answers = eval_result['answers']
        if isinstance(answers, str):
            answers = eval(answers)
        global_evaluation = eval_result['global_evaluation']
        if isinstance(global_evaluation, str):
            global_evaluation = eval(global_evaluation)

        for idx, response in enumerate(answers):
            if response["answer"] == 1:
                scoring_point_scores += scoring_points[idx]["score"]

        assert round(sum(item["score"] for item in scoring_points), 4) == 1, f"Invalid sum of scoring point scores: {sum(item['score'] for item in scoring_points)}"  # noqa: E501

        scores = {
            "semantic_correctness": round(scoring_point_scores, 4),
            "readability": global_evaluation["Clarity and Readability"]["score"],
            "logical_consistency": global_evaluation["Logical Consistency"]["score"],
            "spelling": global_evaluation["Spelling"]["score"],
        }

        scores["scoring_point_all_correct"] = all(item["answer"] for item in answers)
        scores["strict_score"] = int(scores["scoring_point_all_correct"] and scores["spelling"] == 2 and scores["readability"] == 2 and scores["logical_consistency"] == 2)  # noqa: E501
        scores["relaxed_score"] = round(scores["semantic_correctness"] * 0.7 + scores["spelling"] * 0.1 / 2 + scores["readability"] * 0.1 / 2 + scores["logical_consistency"] * 0.1 / 2, 3)  # noqa: E501
        scores['error'] = False

        return scores

    def calculate_score(self, eval_results):
        all_score_keys = ["semantic_correctness", "spelling", "readability", "logical_consistency"]
        all_scores = defaultdict(list)

        for _, eval_result in eval_results.iterrows():
            scores = self.cal_score_single(eval_result)
            data_name = eval_result.get("subject", "all")
            all_scores[data_name].append(scores)

        data_names = sorted(list(all_scores.keys()))

        print("=" * 80)
        print("Each score dimension:")
        for key in all_score_keys:
            score_sum = sum(score[key] for scores in all_scores.values() for score in scores)
            score_avg = score_sum / sum(len(scores) for scores in all_scores.values())
            print(f"- {key}: {round(score_avg, 2)}")

        print("=" * 80)
        print("Each score dimension (average) for each subject:")
        for data_name, scores in all_scores.items():
            if len(scores) == 0:
                continue
            print(f"- {data_name}:")
            for key in all_score_keys:
                score_sum = sum(score[key] for score in scores)
                score_avg = score_sum / len(scores)
                print(f"  {key}: {round(score_avg, 2)}")

        print("-" * 80)
        print("Total number of eval results: ", sum(len(scores) for scores in all_scores.values()))

        print("-" * 80)
        print("Strict score:")
        for data_name in data_names:
            scores = all_scores[data_name]
            if len(scores) == 0:
                continue
            print(f"- {data_name}({len(scores)} samples): {round(sum(score['strict_score'] for score in scores) / len(scores) * 100, 1)}%", end=" ")  # noqa: E501

        avg_strict_score = sum(sum(score['strict_score'] for score in scores) / len(scores) for scores in all_scores.values() if len(scores) > 0) / len(all_scores)  # noqa: E501
        print(f"\nAverage strict score: {round(avg_strict_score * 100, 1)}%")

        print("-" * 80)
        print("Relaxed score:")
        for data_name in data_names:
            scores = all_scores[data_name]
            if len(scores) == 0:
                continue
            print(f"- {data_name}({len(scores)} samples): {round(sum(score['relaxed_score'] for score in scores) / len(scores) * 100, 1)}%", end=" ")  # noqa: E501
        avg_relaxed_score = sum(sum(score['relaxed_score'] for score in scores) / len(scores) for scores in all_scores.values() if len(scores) > 0) / len(all_scores)  # noqa: E501
        print(f"\nAverage relaxed score: {round(avg_relaxed_score * 100, 1)}%")

        avg_semantic_correctness = sum(sum(score['semantic_correctness'] for score in scores) / len(scores) for scores in all_scores.values() if len(scores) > 0) / len(all_scores)  # noqa: E501
        avg_spelling = sum(sum(score['spelling'] for score in scores) / len(scores) for scores in all_scores.values() if len(scores) > 0) / len(all_scores)  # noqa: E501
        avg_readability = sum(sum(score['readability'] for score in scores) / len(scores) for scores in all_scores.values() if len(scores) > 0) / len(all_scores)  # noqa: E501
        avg_logical_consistency = sum(sum(score['logical_consistency'] for score in scores) / len(scores) for scores in all_scores.values() if len(scores) > 0) / len(all_scores)  # noqa: E501
        errors = []
        for data_name in data_names:
            scores = all_scores[data_name]
            if len(scores) == 0:
                continue
            errors.extend([score['error'] for score in scores])
        error_rate = sum(errors) / len(errors)

        return {
            "overall_strict_score": round(avg_strict_score * 100, 2),
            "overall_relaxed_score": round(avg_relaxed_score * 100, 2),
            "semantic_correctness": avg_semantic_correctness,
            "spelling": avg_spelling,
            "readability": avg_readability,
            "logical_consistency": avg_logical_consistency,
            "error_rate": error_rate
        }
